### ARRAY ###
## Ordered List ##
# can mix multiple datatypes
arr1 = [1, 2, 3, 4]
arr2 = ["one", 2, "three", nil, true, "four", 5.0]

puts arr1
puts arr2 [3]
puts arr2 [5]

### HASHES ###
## Or dictionary ##
# key-value pair
# use symbol for key
hash1 = {dog: "barks", cat: "meows", pig: "oinks"}
puts hash1 [:cat]
