### DATA TYPES ###
## 4 basic datatypes -> Number, String, Symbol, Boolean

# Number
num1 = 2   # integer
num2 = 5.5 # float

# String
my_string = "string"

# Symbol
my_symbol = :its_my_symbol

# Boolean
is_true = true   # true
is_false = false # false

puts "Number 1 = #{num1}"
puts "Number 2 = #{num2}"
puts "My string = #{my_string}"
puts "My simbol = #{my_symbol}"
puts "True = #{is_true}"
puts "False = #{is_false}"
